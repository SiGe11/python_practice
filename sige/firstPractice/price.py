# Kriszti, Tomi és Cili édességet vásárol a boltban.  Kriszti két darab csokoládészeletet vásárol, darabját 150 Ft-ért.
# Tomi 5 db cukorkát, aminek darabja 20 Ft-ba kerül. Cili három kekszet vesz, darabját 50 Ft-ért.
# Számítsa ki mindenki esetében a fizetendő összeget!

# Elvárt kimenet:
# Kriszti fizet: <eredmény> Ft-ot
# Tomi fizet: <eredmény> Ft-ot
# Cili fizet: <eredmény> Ft-ot

kriszti = 2 * 150
tomi = 5 * 20
cili = 3 * 50

print(f'Kriszti fizet: {kriszti} Ft-ot')
print(f'Tomi fizet: {tomi} Ft-ot')
print(f'Cili fizet: {cili} Ft-ot')
